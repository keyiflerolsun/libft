#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*res;
	t_list	*new;
	void	*cnt;

	res = NULL;
	while (lst)
	{
		cnt = f(lst->content);
		new = ft_lstnew(cnt);
		if (!new)
		{
			del(cnt);
			ft_lstclear(&res, del);
			return (NULL);
		}
		ft_lstadd_back(&res, new);
		lst = lst->next;
	}
	return (res);
}
